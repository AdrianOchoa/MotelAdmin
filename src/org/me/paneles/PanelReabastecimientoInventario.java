    private String[] getProductos() {
        try {
            DataBaseHelper dbh = new DataBaseHelper(getDbUser(), getDbPass());
            dbh.iniciarConexion();
            String[] productos;
            productos = dbh.getProductos();
            dbh.cerrarConexion();
            return productos;
        } catch (ClassNotFoundException | SQLException ex) {
            String[] rs = new String[1];
            rs[0] = "No hay productos en el inventario.";
            return rs;
        }
    }

