    public boolean datosValidos () {
        return camposLlenos ()
                && existenciaValida()
                && precioUnitarioValido()
                && precioTotalValido();
    }
    
    private boolean precioTotalValido () {
        try {
            Double.parseDouble(jtfPrecioTotal.getText());
            return true;
        } catch (NumberFormatException ex) {
            Message.showErrorMessage("Error en el campo de la existencia. "
                    + "Debe ser un número doble válido.");
            return false;
        }
    }
    
    private boolean precioUnitarioValido () {
        try {
            Double.parseDouble(jtfPrecioUnitario.getText());
            return true;
        } catch (NumberFormatException ex) {
            Message.showErrorMessage("Error en el campo de la existencia. "
                    + "Debe ser un número doble válido.");
            return false;
        }
    }
    
    private boolean existenciaValida () {
        try {
            Integer.parseInt(jtfExistencias.getText());
            return true;
        } catch (NumberFormatException ex) {
            Message.showErrorMessage("Error en el campo de la existencia. "
                    + "Debe ser un número doble válido.");
            return false;
        }
    }
    
    private boolean camposLlenos () {
        if(!jtfMarca.getText().isEmpty() 
                && !jtfExistencias.getText().isEmpty() 
                && !jtfPrecioUnitario.getText().isEmpty() 
                && !jtfPrecioTotal.getText().isEmpty()) {
            return true;
        } else {
            Message.showErrorMessage("Falta de llenar algunos campos.");
            return false;
        }
    }

