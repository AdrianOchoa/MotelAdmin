    /**
     * 
     * @return un arreglo de cadenas con las habitaciones ocupadas
     */
    public String[] getHabitacionesOcupadas() {
        try {
            DataBaseHelper dbh = new DataBaseHelper(getDbUser(), getDbPassword());
            dbh.iniciarConexion();
            String[] habitaciones;
            habitaciones = dbh.getHabitacionesOcupadas();
            dbh.cerrarConexion();
            return habitaciones;
        } catch (ClassNotFoundException | SQLException ex) {
            String[] rs = new String[1];
            rs[0] = "No hay habitaciones disponibles.";
            return rs;
        }
    }

